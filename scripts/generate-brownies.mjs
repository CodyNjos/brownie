import { readdirSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join, parse } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const imagesDir = join(repoRoot, "public", "brownies");
const outFile = join(repoRoot, "src", "brownies.ts");

const IMAGE_EXTS = new Set([".jpg", ".jpeg", ".png", ".webp", ".gif", ".avif"]);

function toDisplayName(basename) {
  return basename
    .replace(/[-_]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .split(" ")
    .map((word) => (word ? word[0].toUpperCase() + word.slice(1) : word))
    .join(" ");
}

function readImageFiles() {
  if (!existsSync(imagesDir)) {
    mkdirSync(imagesDir, { recursive: true });
    return [];
  }
  return readdirSync(imagesDir)
    .filter((file) => {
      const { ext, name } = parse(file);
      if (!ext || !IMAGE_EXTS.has(ext.toLowerCase())) return false;
      if (name.startsWith(".")) return false;
      return true;
    })
    .sort((a, b) => {
      const aFirst = parse(a).name === "I Did Not Eat Any Brownies" ? -1 : 0;
      const bFirst = parse(b).name === "I Did Not Eat Any Brownies" ? -1 : 0;
      if (aFirst !== bFirst) return aFirst - bFirst;
      return a.localeCompare(b);
    });
}

const files = readImageFiles();

const entries = files.map((file) => {
  const { name } = parse(file);
  return {
    id: name,
    name: toDisplayName(name),
    image: `/brownies/${file}`,
  };
});

const header = `// AUTO-GENERATED by scripts/generate-brownies.mjs — do not edit by hand.
// Regenerated on every \`npm run dev\` / \`npm run build\` from files in public/brownies/.

export type Brownie = {
  id: string;
  name: string;
  image: string;
};

export const BROWNIES: Brownie[] = ${JSON.stringify(entries, null, 2)};
`;

writeFileSync(outFile, header, "utf8");
console.log(`generate-brownies: wrote ${entries.length} brownie(s) to src/brownies.ts`);
